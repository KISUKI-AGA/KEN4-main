// Helper to generate ruby html. Input format: "我(ㄨㄛˇ)們(˙ㄇㄣ)"
const withZhuyin = (id: number, raw: string, img: string): Question => {
  let text = "";
  let ruby_html = "";
  
  // Regex to match Character(Zhuyin) or just Character
  // Matches: Char followed by (Zhuyin)
  // or Char not followed by (
  const parts = raw.split(/([^\u3105-\u3129\u02CA\u02C7\u02CB\u02D9()]+(?:\([\u3105-\u3129\u02CA\u02C7\u02CB\u02D9]+\))?)/).filter(p => p.trim());

  parts.forEach(part => {
    const match = part.match(/(.+)\((.+)\)/);
    if (match) {
      const char = match[1];
      const zy = match[2];
      text += char;
      ruby_html += `<ruby>${char}<rt>${zy}</rt></ruby>`;
    } else {
      // Handle punctuation or plain text
      text += part;
      ruby_html += part;
    }
  });

  return {
    id,
    text,
    image_filename: img,
    ruby_text: ruby_html
  };
};

